"""
创建平滑的广东省外轮廓mask（只需要外边界，不需要内部细节）
"""

from PIL import Image, ImageDraw
import numpy as np

def create_smooth_guangdong_mask():
    """
    创建平滑的广东省外轮廓
    只保留外边界，内部完全填充
    """
    # 创建画布
    width, height = 640, 480
    mask = Image.new('L', (width, height), 255)  # 白色背景
    draw = ImageDraw.Draw(mask)
    
    # 广东省外轮廓坐标（平滑版本，只保留外边界）
    # 按照顺时针方向，从北到南
    outline = [
        # 北部边界（湖南交界）
        (200, 30),
        (280, 25),
        (350, 30),
        (420, 28),
        (500, 35),
        (580, 40),
        
        # 东部边界（梅州、潮州、汕头）
        (650, 50),
        (720, 70),
        (780, 100),
        (830, 140),
        (870, 190),
        (900, 250),
        (910, 310),
        
        # 东南部（汕尾、揭阳、汕头沿海）
        (900, 370),
        (880, 420),
        (850, 460),
        
        # 南部沿海（深圳、珠海、澳门）
        (800, 490),
        (740, 510),
        (680, 525),
        (620, 535),
        (560, 540),
        
        # 西南部（阳江、茂名、湛江）
        (500, 535),
        (440, 545),
        (380, 560),
        (320, 580),
        (260, 605),
        (200, 630),
        
        # 湛江半岛（雷州半岛）
        (160, 650),
        (130, 640),
        (110, 620),
        (100, 590),
        (105, 560),
        
        # 西部边界（云浮、肇庆）
        (120, 530),
        (140, 500),
        (160, 470),
        (180, 440),
        
        # 西北部（清远、韶关）
        (170, 400),
        (160, 360),
        (150, 320),
        (145, 280),
        (150, 240),
        (160, 200),
        (170, 160),
        (180, 120),
        (190, 80),
        (200, 30),  # 闭合
    ]
    
    # 绘制实心黑色轮廓（内部完全填充）
    draw.polygon(outline, fill=0)  # 黑色=0表示填充区域
    
    # 保存mask
    mask.save('guangdong_smooth_mask.png')
    print(f"✅ 已创建平滑的广东省外轮廓 mask (640x480)")
    print(f"   轮廓点数: {len(outline)}")
    
    return mask

if __name__ == '__main__':
    create_smooth_guangdong_mask()
